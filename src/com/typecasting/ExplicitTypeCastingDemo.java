package com.typecasting;

public class ExplicitTypeCastingDemo {
 public static void main(String[] args) {
	int i=130;
	byte b=(byte)i;
	System.out.println(b);
	int i1=150;
	short s=(short)i1;
	System.out.println(s);
    byte b1=(byte)i1;
    System.out.println(b1);
    float f=100.9899f;
    int i2=(int)f;
    System.out.println(i2);
}
}
